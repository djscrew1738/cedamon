def _graph_gap(
    ctx: RuleContext,
    query_key: str,
    cypher: str,
    tool_name: str,
    rule_id: str,
    name: str,
    rationale: str,
    priority: int,
    cost_score: float,
    suggested_args: dict[str, Any] | None = None,
) -> list[HeuristicRule]:
    """Helper to run a graph coverage-gap query and return a rule if gaps exist."""
    if not ctx.graph_client:
        return []
    if tool_name in ctx.already_run:
        return []
    if ctx.phase != "informational":
        return []
    try:
        rows = ctx.graph_client.query(cypher)
    except Exception as exc:
        import logging
        logging.getLogger(__name__).warning("Graph gap query %s failed: %s", query_key, exc)
        return []
    if not rows:
        return []
    return [
        HeuristicRule(
            id=rule_id,
            name=name,
            category="coverage",
            priority=priority,
            tool_name=tool_name,
            rationale=rationale,
            phase="informational",
            task_cluster="recon",
            cost_score=cost_score,
            suggested_args=dict(suggested_args or {}),
        )
    ]


